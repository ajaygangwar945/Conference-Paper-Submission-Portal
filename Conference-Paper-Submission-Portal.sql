-- Trigger to update last_modified
CREATE OR REPLACE TRIGGER trg_papers_last_modified
BEFORE UPDATE ON papers
FOR EACH ROW
BEGIN
    :NEW.last_modified := SYSDATE;
END;
/

-- Procedure to submit paper with validation
CREATE OR REPLACE PROCEDURE submit_paper(
    p_title         IN VARCHAR2,
    p_abstract      IN CLOB,
    p_keywords      IN VARCHAR2,
    p_submitter_id  IN NUMBER,
    p_conference_id IN NUMBER,
    p_file_path     IN VARCHAR2,
    p_file_name     IN VARCHAR2
) AS
    v_max_papers  NUMBER;
    v_user_papers NUMBER;
BEGIN
    SELECT max_papers_per_user INTO v_max_papers
    FROM conferences
    WHERE conference_id = p_conference_id;

    SELECT COUNT(*) INTO v_user_papers
    FROM papers
    WHERE submitter_id = p_submitter_id
      AND conference_id = p_conference_id;

    IF v_user_papers >= v_max_papers THEN
        RAISE_APPLICATION_ERROR(-20001, 'User has reached the maximum paper submissions for this conference.');
    ELSE
        INSERT INTO papers (title, abstract, keywords, submitter_id, conference_id, status_id, file_path, file_name)
        VALUES (p_title, p_abstract, p_keywords, p_submitter_id, p_conference_id, 1, p_file_path, p_file_name);
    END IF;
END;
/

-- Procedure to change paper status
CREATE OR REPLACE PROCEDURE change_paper_status(
    p_paper_id   IN NUMBER,
    p_new_status IN NUMBER
) AS
BEGIN
    UPDATE papers
    SET status_id = p_new_status
    WHERE paper_id = p_paper_id;
END;
/
