-- Create Tables
CREATE TABLE users (
    user_id        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fullname       VARCHAR2(100) NOT NULL,
    email          VARCHAR2(100) UNIQUE NOT NULL,
    password_hash  VARCHAR2(255) NOT NULL,
    role           VARCHAR2(20) CHECK (role IN ('student', 'faculty', 'admin', 'reviewer')),
    institution    VARCHAR2(100),
    department     VARCHAR2(100),
    created_date   DATE DEFAULT SYSDATE
);

CREATE TABLE conferences (
    conference_id        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    conference_name      VARCHAR2(200) NOT NULL,
    conference_date      DATE NOT NULL,
    submission_deadline  DATE NOT NULL,
    location             VARCHAR2(100),
    description          CLOB,
    max_papers_per_user  NUMBER DEFAULT 3,
    is_active            NUMBER(1) DEFAULT 1 CHECK (is_active IN (0, 1))
);

CREATE TABLE paper_status (
    status_id    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    status_name  VARCHAR2(50) NOT NULL,
    description  VARCHAR2(200)
);

CREATE TABLE papers (
    paper_id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title             VARCHAR2(500) NOT NULL,
    abstract          CLOB,
    keywords          VARCHAR2(500),
    submission_date   DATE DEFAULT SYSDATE,
    last_modified     DATE DEFAULT SYSDATE,
    submitter_id      NUMBER REFERENCES users(user_id),
    conference_id     NUMBER REFERENCES conferences(conference_id),
    status_id         NUMBER REFERENCES paper_status(status_id),
    file_path         VARCHAR2(500),
    file_name         VARCHAR2(200),
    valid_submission  NUMBER(1) DEFAULT 1 CHECK (valid_submission IN (0, 1)),
    review_score      NUMBER(3,2),
    final_decision    VARCHAR2(50)
);

